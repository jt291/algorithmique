# -*- coding: utf-8 -*-
 
def infix (t) :
    if t != [] :
        infix (t[1])
        print (t[0], end=' ')
        infix (t[2])
    else :
        print (0, end=' ')

    return

#-----------------------------------------------------------------------
def postfix (t) :
    if t != [] :
        postfix (t[1])
        postfix (t[2])
        print (t[0], end=' ')
    else :
        print (0, end=' ')

    return

